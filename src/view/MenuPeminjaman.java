    private void setColWidht(int[] index, int[] panjang, JTable tbl) {
        TableColumnModel tblModel = tbl.getColumnModel();
        for (int i = 0; i < index.length; i++) {
            tblModel.getColumn(index[i]).setPreferredWidth(panjang[i]);
            tblModel.getColumn(index[i]).setMaxWidth(panjang[i]);
            tblModel.getColumn(index[i]).setMinWidth(panjang[i]);
        }
    }

